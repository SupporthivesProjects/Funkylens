<script>

/* =========================
   TABS
========================= */

const tabBtns = document.querySelectorAll(".rj-tab-btn");
const tabPanes = document.querySelectorAll(".rj-tab-pane");

tabBtns.forEach(btn => {

    btn.addEventListener("click", function(){

        tabBtns.forEach(item => {
            item.classList.remove("active");
        });

        tabPanes.forEach(item => {
            item.classList.remove("active");
        });

        this.classList.add("active");

        const target = this.getAttribute("data-tab");

        document.getElementById(target).classList.add("active");

    });

});

/* =========================
   MOBILE ACCORDION
========================= */

const faqQuestions = document.querySelectorAll(".rj-faq-question");

faqQuestions.forEach(question => {

    question.addEventListener("click", function(){

        if(window.innerWidth <= 768){

            const parent = this.closest(".rj-faq-item");

            parent.classList.toggle("active");

        }

    });

});

</script>
